# ── Translation function ───────────────────────────────────────────
_tr() {
	local key="$1"
	shift
	local lang="${JINX_LANG:-en}"
	local full_key="${lang}.${key}"
	local msg="${_TR[$full_key]:-${key}}"
	if [[ $# -gt 0 ]]; then
		# shellcheck disable=SC2059
		printf "$msg" "$@"
	else
		echo -n "$msg"
	fi
}

# ── Help: _tr_echo works like _tr but adds newline ────────────────
_tr_echo() {
	_tr "$@"
	echo
}

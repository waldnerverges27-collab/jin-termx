# ── _tr function ───────────────────────────────────────────────────
_tr() {
	local key="$1"
	shift
	local full_key="${JINX_LANG:-en}.${key}"
	local msg="${_TR[$full_key]}"
	# fallback: try English
	if [[ -z "$msg" && "${JINX_LANG:-en}" != "en" ]]; then
		msg="${_TR[en.${key}]}"
	fi
	msg="${msg:-$key}"
	if [[ $# -gt 0 ]]; then
		printf "$msg" "$@"
	else
		echo -n "$msg"
	fi
}

_tr_echo() {
	_tr "$@"
	echo
}
